package Basic;

public class if_else_statement {
	public static void main(String[] args) {
		int score;
		
		score = 100;
		if(score >= 90.0)
			System.out.print("A");
		else if(score >= 80.0)
			System.out.print("B");
		else if(score >= 70.0)
			System.out.print("C");
		else if(score >= 60.0)
			System.out.print("D");
	}

}
